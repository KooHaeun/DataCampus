package com.example.chopchop;

public class dotData {
    Float um;
    Float gum;

    public dotData(Float um, Float gum) {
        this.um = um;
        this.gum = gum;
    }

    public Float getUm() {
        return um;
    }

    public void setUm(Float um) {
        this.um = um;
    }

    public Float getGum() {
        return gum;
    }

    public void setGum(Float gum) {
        this.gum = gum;
    }
}
